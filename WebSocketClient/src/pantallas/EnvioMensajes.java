    private void jButtonEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEnviarActionPerformed
        String destinatario = this.jComboBoxUsuarios.getSelectedItem().toString();
        String asunto = this.jTextFieldAsunto.getText();
        String remitente = this.jTextFieldRemitente.getText();
        String mensaje = this.jTextAreaCuerpoMensaje.getText();
        Mensaje mensajeEnviar = new Mensaje(nombre, destinatario, asunto, remitente, mensaje);
        try {
            cliente.sendMessage(mensajeEnviar);
            limpiarCampos();
        } catch (Exception ex) {
            System.out.println("Error; " + ex.getMessage());
        }
    }//GEN-LAST:event_jButtonEnviarActionPerformed
