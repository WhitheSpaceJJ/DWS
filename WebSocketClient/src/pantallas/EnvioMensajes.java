    public void update(Cliente[] arg) {
        try {
            Cliente[] clienteArreglo = (Cliente[]) arg;
            clientes = new ArrayList<>();
            for (Cliente cliente1 : clienteArreglo) {
                clientes.add(cliente1);
            }
            llenarCombo();
        } catch (Exception e) {
            System.out.println("Error; " + e.getMessage());
        }

    }

    public void llenarCombo() {
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        if (clientes.size() == 1) {
            model.addElement(nombre);
            this.jButtonEnviar.setEnabled(false);
            this.jComboBoxUsuarios.setEnabled(false);
        } else {
            model.addElement("Todo el Publico");
            for (int i = 0; i < clientes.size(); i++) {
                Cliente get = clientes.get(i);
                if (!get.getNombreCliente().equalsIgnoreCase(nombre)) {
                    model.addElement(get.getNombreCliente());
                }
            }
            this.jButtonEnviar.setEnabled(true);
            this.jComboBoxUsuarios.setEnabled(true);

        }
        this.jComboBoxUsuarios.setModel(model);
    }

